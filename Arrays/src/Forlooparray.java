public class Forlooparray{
    public static void main(String[] args) {
            String[] animals = {"Goat", "Sheep", "Cattle", "Pigeon"};
            for (int i = 0; i < animals.length; i++) {
                System.out.println(animals[i]);
            }
        }
    }

//Above is an example of for loop array and also the specification of how many times you want the length to move.
