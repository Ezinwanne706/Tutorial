public class Long {
    public static void main(String[] args) {
        long myNum = 15000000000L;
        System.out.println(myNum);
    }
}
// Above is an example of the long keyword and, The long keyword is a data type that can store whole numbers from -9223372036854775808 to 9223372036854775808.
// NB: Always put l after entering this data type because it tells the compiler that we have a long literal